keymap_to_ascii: ; takes keycode in a, returns ASCII in a
    exx
    ld c, a
    ld b, 0
    ld hl, key_map
    add hl, bc
    ld a, (hl)
    exx
    ret

read_input:
    call clear_buffer
    ld hl, STRING_BUFFER    ; buffer pointer in hl
read_loop:
    call print_buffer
    call 00026h             ; call Monitor routine to read keycode into a
    call keymap_to_ascii    ; convert keycode to ASCII
    cp 13                   ; if enter, we're done
    jr z, key_return
    cp 8
    jr z, key_backspace
    cp 0
    jr z, key_invalid
add_key:
    ld b, a                 ; store ASCII code in b
    ld a, (STRING_BUFFER+MAX_STRING_LENGTH) & 0xFF ; check if buffer is at MAX
    cp l                    ; l holds lower byte of buffer pointer
    jr z, key_invalid
    ld (hl), b              ; store ASCII code in buffer  
    inc hl
    jr read_loop
key_return:
    ret
key_backspace:
    ld a, STRING_BUFFER & 0xFF ; if buffer is still empty, then beep (invalid)
    cp l                    ; l holds lower byte of buffer pointer
    jr z, key_invalid
    dec hl
    ld (hl), 0              ; remove last char
    jr read_loop
key_invalid:
    call $0032              ; beep
    jr read_loop

