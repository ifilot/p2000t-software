    DS 15, 0

    ; Because Fraxxon's size is more than 16KiB, I had to compress the bytes
    ; using LZ4 algorithm. The LZ4 decompression routine for Z80 is included 
    ; from 'libs/LZ4_Z80.asm'.
    ; Also, I had to simulate several Basic instructions to start the game.

start:
    ld a, ($605C)           ; Fraxxon needs >=32K RAM. check if we have enough
    cp 1
    jr z, not_enough_ram

    ; decompress Fraxxon game
    ld hl, fraxxon_bytes    ; pointer to LZ4 compressed source data
    ld de, $6547            ; pointer to decompressed destination data
    call LZ4_Decompress
    call check_LZ4_error

    ; set Basic pointer to end of program
    ld hl, BASIC_START + FRAXXON_SIZE
    ld ($6405), hl

    ; simulate Basic's instruction CLEAR 40, ...
    ld hl, $C5C8            ; $C5C8 = $6547+24505+200
    ld ($63B8), hl
    ld sp, $C5A0            ; stack pointer set to $C5A0 (= $C5C8 - 40)

    ; simulate call to USR(0)
    ld a,2
    ld ($63B6), a
    ld hl,0
    ld ($650D),hl 
    ld hl, $650D

    jp $6800                ; Now finally start the game :)

not_enough_ram:
    ld hl, msg_mem_err
    call printmsg
    jp endless_loop

check_LZ4_error:
    and a
    ret z                   ; return if no error found
    ld e,a
    ld hl, msg_lz4_err
    call printmsg
    ld a,e
    add a,$30               ; print error code
    ld (bc), a
endless_loop:
    jr endless_loop

