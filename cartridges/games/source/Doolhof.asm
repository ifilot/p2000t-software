    ; the end of the game, for which I made a custom implementation.

start:
    ld hl, doolhof_bytes    ; Source address
    ld de, $6547            ; Destination address
    ld bc, 8726             ; Number of bytes to copy
    ldir                    ; Copy BC bytes from (HL) to (DE)

    ; copy game code to end of memory
    ld hl, $875D
    ld de, $9fff
    ld bc, $1449
    lddr
intro_screen:
    call $91CA              ; Show intro screen
    cp 3                    ; if A=3, 'J' key was pressed
    jr nz, intro_screen     ; otherwise show intro screen again
strart_game:
    call clear_screen
    call $92DF              ; Start game
    
    ; Game ended, with reason in register A
    cp 0                    ; if A=0, <STOP> was pressed twice
    jr z, intro_screen
    cp 1                    ; if A=1, player went out through entrance  
    ld hl, msg_vals_spel
    jr z, display_msg
    cp 2                    ; if A=2, player found exit
    ld hl, msg_gefeliciteerd
    jr z, display_msg
    cp 4                    ; if A=4, player took too long
    ld hl, msg_duurde_te_lang
    jr z, display_msg
    jp intro_screen

display_msg:
    call clear_screen
    ld bc, $5000 + $50*8
    call printmsg_atloc
    call $0AF2              ; wait for 500ms
    call $0AF2              ; wait for 500ms
    ld bc, $5000 + $50*12
    ld hl, msg_druk_op_toets
    call printmsg_atloc
    call $0026              ; wait for key press
    jp strart_game          ; restart game (skip intro screen)

