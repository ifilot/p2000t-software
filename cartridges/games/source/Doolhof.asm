msg_duurde_te_lang:
    DB $06,$0D,"Helaas, meer dan 1000 strafpunten...",$FF
msg_druk_op_toets:
    DB $03,$08,"Druk een toets om opnieuw te spelen.",$FF

; hl - pointer to string
; bc - screen location address
printmsg_atloc:
    ld a,(hl)
    cp 255
    ret z
    ld (bc),a
    inc hl
    inc bc
    jr printmsg_atloc

clear_screen:
    exx
    ld hl, $5000
    ld a, 24
    call $0035              ; clear 24 lines of into_screen
    exx
    ret

